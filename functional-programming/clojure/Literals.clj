(println "Numbers")
(println 42) ; integer
(println -1.5) ; float
(println 22/7) ; ratio
(println "Characters types")
(println \a) ; character
(println "Strings") ; string
(println #"[0-9]") ; regex
(println "Symbols and keywords")
(println true) ; boolean
(println nil) ; nil
(println :keyword) ; keyword
(println :keyword/alpha) ; keyword with namespace
(println clojure.core/+) ; namespace
(println "Literal collections")
(println [1 2 3]) ; vector
(println '(1 2 3)) ; list
(println {:a 1 :b 2}) ; map
(println #{1 2 3}) ; set
